const iconsDir = path.join(__dirname, 'components', 'icons');

if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

Object.entries(icons).forEach(([name, content]) => {
  const filePath = path.join(iconsDir, `${name}.js`);
  fs.writeFileSync(filePath, content.trim());
});

const indexContent = Object.keys(icons)
  .map(name => `export { default as ${name} } from './${name}';`)
  .join('\n');

fs.writeFileSync(path.join(iconsDir, 'index.js'), indexContent);

